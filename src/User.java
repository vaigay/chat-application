    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (chatUser == null) {
            mess("Mời chọn người chat");
            return;
        }
        try {
            dos.writeUTF("checkUser");
            dos.writeUTF(chatUser);
            int check = dis.readInt();
            if (check == 0) {
                mess("Người dùng hiện đã offline");
                return;
            }
            if(UserChatWith.contains(chatUser)){
                mess("Bạn đang chat với " +chatUser);
                return;
            }
        } catch (IOException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            UserChatWith.add(chatUser);
            new Userchatting(chatUser, userName,UserChatWith).setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        int dialog = JOptionPane.YES_NO_OPTION;
        int res = JOptionPane.showConfirmDialog(this, "Thoát", "Bạn muốn thoát ứng dụng?", dialog);
        if (res == JOptionPane.YES_OPTION) {
            try {
                dos.writeUTF("close");
                dos.close();
                dis.close();
            } catch (IOException ex) {
                Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        } else {
            this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        }
    }//GEN-LAST:event_formWindowClosing

