    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        roomName = jTextField1.getText();
        if (roomName.equals("") || roomName == null)
            mess("Tên phòng không được bỏ trống");
        else {
            try {
                dos.writeUTF("newRoom");
                dos.writeUTF(roomName);
                System.out.println(roomName);
                int check = dis.readInt();         
                if (check == 0) {
                    RoomChat roomChat = new RoomChat(userName,roomName,InRoomChat);
                    InRoomChat.add(roomName);
                    roomChat.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Trùng tên phòng đã được tạo");
                }
                jTextField1.setText("");
            } catch (IOException ex) {
                Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            dos.writeUTF("sendListRoom");
            DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
            m.getDataVector().removeAllElements();
            String x = dis.readUTF();
            int size = dis.readInt();
            while(!x.equals("endRoom")){
                Object[] e = new Object[]{x,size};                
                 m.addRow(e);
                x = dis.readUTF();
                size = dis.readInt();
            }
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(roomName == null){
            mess("Mời chọn phòng chat");
            return;
        }
        try {
            if(InRoomChat.contains(roomName)){
                mess("Bạn đang ở trong phòng chat này rồi!");
                return;
            }
            else{
                InRoomChat.add(roomName);
                new RoomChat(userName, roomName,InRoomChat).setVisible(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
           
    }//GEN-LAST:event_jButton3ActionPerformed

